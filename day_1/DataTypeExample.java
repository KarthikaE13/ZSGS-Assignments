package day_1;
//Write a program in which, declare all data types like integer, double, float, long integer, character and byte data and print them.
public class DataTypeExample {
	public static void main(String[] args) {
		int myInt = 100;
        long myLong = 1234567890123L;  
        byte myByte = 120;             

        // Floating point types
        float myFloat = 12.34f;       
        double myDouble = 1234.5678;

        // Character
        char myChar = 'A';

        // Boolean
        boolean myBool = true;

        // Print all data types
        System.out.println("Integer value (int): " + myInt);
        System.out.println("Long value (long): " + myLong);
        System.out.println("Byte value (byte): " + myByte);
        System.out.println("Float value (float): " + myFloat);
        System.out.println("Double value (double): " + myDouble);
        System.out.println("Character value (char): " + myChar);
        System.out.println("Boolean value (boolean): " + myBool);
		
	}
	

}
